//PRIMITIVE DATATYPES//


/*there are 5 number of datatypes
1 numbers
2 strings
3 boolean*/

//below is number datatype
let a = 5;
console.log(a);
console.log(typeof a);

//below is string datatype
let Name = "Pratham" ;
console.log(Name);
console.log(typeof Name);

//below is boolean datatype
let LoggedIn = true;
console.log(LoggedIn);
console.log(typeof LoggedIn);

/*NULL and UNDEFINED are also primitve datatypes 
NULL represents an invalid object 
UNDEFINED represents a variable that has been declared but not assigned a value is automatically assigned the value*/

//below is NULL datatype
let temperature = null;
console.log(temperature);
console.log(typeof temperature);

//below is UNDEFINED datatype
let score;
console.log(score);
console.log(typeof score);

